package org.sensorhub.api.persistence;

import org.sensorhub.api.common.Event;
import org.sensorhub.api.persistence.StorageEvent.Type;


/**
 * <p>
 * Base class for events generated by storage modules
 * </p>
 *
 * @author Alex Robin <alex.robin@sensiasoftware.com>
 * @since Nov 12, 2010
 */
public class StorageEvent extends Event<Type>
{
    
    /**
     * Possible event types for a StorageEvent
     */
    public enum Type
    {
        STORE,
        UPDATE,
        DELETE,
        GET
    };


    public StorageEvent(long timeStamp, IStorageModule<?> source, Type type)
    {
        this.timeStamp = timeStamp;
        this.source = source;
        this.sourceModuleID = source.getLocalID();
        this.type = type;
    }
    
    
    public StorageEvent(long timeStamp, IRecordDataStore<?,?> source, Type type)
    {
        this.timeStamp = timeStamp;
        this.source = source;
        this.sourceModuleID = source.getParentStorage().getLocalID();
        this.type = type;
    }
    
    
    public String getStorageId()
    {
        return sourceModuleID;
    }


    @Override
    public Type getType()
    {
        return type;
    }
}
